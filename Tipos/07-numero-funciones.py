# Importar el módulo math para funciones matemáticas avanzadas
import math

print(round(1.4))  # Redondea el número al entero más cercano
print(round(1.5))
print(round(1.6))

print(abs(-77))  # Devuelve el valor absoluto del número
print(abs(55))

print(math.ceil(1.1))  # Redondea hacia arriba al entero más cercano
print(math.floor(1.9))  # Redondea hacia abajo al entero más cercano
print(math.isnan(23))  # Verifica si el número es NaN (Not a Number)
# print(math.isnan("23")) 
print(math.pow(2, 3))  # Calcula 2 elevado a la potencia de 3
print(math.sqrt(16))  # Calcula la raíz cuadrada de 16
print(math.factorial(5))  # Calcula el factorial de 5 (5!)

