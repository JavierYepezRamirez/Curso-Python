# Tipos de variables en Python
# string: Cadena de caracteres, por ejemplo, "Hola Mundo"
# int: Enteros, por ejemplo, 5, -10
# float: Números decimales, por ejemplo, 3.14, -0.001
# bool: Booleanos, True o False
# list: Listas, por ejemplo, [1, 2, 3], ["a", "b", "c"]
# dict: Diccionarios, por ejemplo, {"clave": "valor", "edad": 30}


# Como no definir una variable
#
