# Como definir variables
# Las variables son espacios de memoria que se utilizan para almacenar datos.
# Los nombres de las variables deben seguir ciertas reglas:
# - Deben comenzar con una letra o un guion bajo (_).
# - Pueden contener letras, números y guiones bajos.
