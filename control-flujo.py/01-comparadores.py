# Inicio: 23/06/2025

print(1 > 2) # Falso, porque 1 no es mayor que 2
print(1 < 2) # Verdadero, porque 1 es menor que 2
print(1 <= 2) # Verdadero, porque 1 es menor o igual que 2
print(1 >= 2) # Falso, porque 1 no es mayor o igual que 2
print(1 == 2) # Falso, porque 1 no es igual a 2
print(1 != 2) # Verdadero, porque 1 es diferente de 2 